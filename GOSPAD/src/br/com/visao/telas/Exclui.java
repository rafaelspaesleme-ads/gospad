    private void chamarTelas(JInternalFrame p) {

        Principal.getPainel().add(p);
        p.setVisible(true);

        int x = (Principal.getPainel().getWidth() / 2) - (p.getWidth() / 2);
        int y = (Principal.getPainel().getHeight() / 2) - (p.getHeight() / 2);
        p.setVisible(true);
        p.setLocation(x, y);
        p.toFront();
        p.show();
    }

